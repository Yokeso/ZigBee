#include <iocc2530.h>
#include "hal_defs.h"
#include "hal_types.h"

//*****************************************************************************/
//函数名：HalGasFlameInit
//功  能：初始化气体和火焰检测信号的IO管脚
//参  数：无
//返回值：无
//*****************************************************************************/
void HalGasFlameInit()
{
    P1DIR &= ~(BV(4) + BV(3));
}

//*****************************************************************************/
//函数名：HalGasCheck
//功  能：检测MQ2的输出电平，高电平表示检测到了烟雾或者有害气体，
//参  数：无
//返回值：返回1表示检测到有害气体，0表示一切正常
//*****************************************************************************/
uint8 HalGasCheck()
{
    if (P1_3)
    {
        return 0;
    }
    else 
    {
        return 1;
    }
}

//*****************************************************************************/
//函数名：HalFlameCheck
//功  能：检测火焰传感器输出电平，高电平表示检测到火焰，低电平表示一切正常
//参  数：无
//返回值：1表示检测到了火焰，0表示一切正常
//*****************************************************************************/
uint8 HalFlameCheck()
{
    if (P1_4)
    {
        return 0;
    }
    else 
    {
        return 1;
    }
}