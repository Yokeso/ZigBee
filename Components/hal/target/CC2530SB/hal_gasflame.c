//              同时可以检测烟雾，例如抽烟产生的烟雾；
//           (2)火焰传感器可以检测对应方向上的火焰；
//           (3)通过调节两个传感器的电位器，可以调节传感器的灵敏度；
//              电位器R8调节的是MQ2烟雾传感器的灵敏度；
//              电位器R13调节的是火焰传感器的灵敏度；
//
